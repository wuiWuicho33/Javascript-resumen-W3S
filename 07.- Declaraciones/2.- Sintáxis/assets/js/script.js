//El punto y la coma sirven para separar las declaraciones 
var a, b, c;
a = 5;
b = 6;
c = a + b;
document.getElementById("demo-01").innerHTML = c;

// Javascript ignora los espacios en blanco
// Las siguientes líneas son equivalentes:
var person = "Hege";
var person="Hege";

// Una buen práctica es poner espacios alrededor de los operadores
var y = 2;
var z = 1;
var x = y + z;

// Saltos de línea
// El mejor lugar para romper una declaración es después de un operador o una coma. 
document.getElementById("demo-03").innerHTML =
"¡Hola Rony!";

